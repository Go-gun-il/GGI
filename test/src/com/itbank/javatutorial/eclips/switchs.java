package com.itbank.javatutorial.eclips;

public class switchs {

	public static void main(String[] args)
	{
		switch (5)
		{
			case 1:
				System.out.println("one");
				break;
			case 2:
				System.out.println("two");
				break;
			case 3:
				System.out.println("three");
				break;
			default:
				System.out.println("else");
		}
	}

}
